     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $rack = Rack::query()
            ->when($request->input('search'), function ($query, $search) {
             $query->where('rack_number', 'like' , "%" . $search . "%");
            })->paginate('10');
        return  view('settings.rack.index', compact('rack'));
    }

